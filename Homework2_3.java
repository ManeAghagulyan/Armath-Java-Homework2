public class Homework2_3 {
    public static void main(String[] args) {
        //Arithmetic operations with two numbers

        int firstNumber = 100;
        int secondNumber = 15;

        int sumOfNumbers = firstNumber + secondNumber;
        System.out.println("Sum of " + firstNumber + " and " + secondNumber +" = " + sumOfNumbers);

        int subOfNumbers = firstNumber - secondNumber;
        System.out.println("Sub of " + firstNumber + " and " + secondNumber +" = " + subOfNumbers);

        int mulOfNumbers = firstNumber * secondNumber;
        System.out.println("Mul of " + firstNumber + " and " + secondNumber +" = " + mulOfNumbers);

        double divOfNumbers = firstNumber / (double)secondNumber;
        System.out.println("Div of " + firstNumber + " and " + secondNumber +" = " + divOfNumbers);

        int remOfNumbers = firstNumber % secondNumber;
        System.out.println("Mul of " + firstNumber + " and " + secondNumber +" = " + remOfNumbers);
    }
}
