public class Homework2_2 {
    public static void main(String[] args){
        // Assigning values to variables and print values
        // byte variable
        byte byteVar =  2;
        System.out.println("Value of byte variable \t" + byteVar);

        // short variable
        short shortVar = 1237;
        System.out.println("Value of short variable \t" + shortVar);

        // integer variable
        int intVar = 189025;
        System.out.println("Value of integer variable \t" + intVar);

        // long  variable
        long longVar = 29755L;
        System.out.println("Value of long variable \t" + longVar);

        // float variable
        float floatVar = 29.5f;
        System.out.println("Value of float variable \t" + floatVar);

        // double variable
        double doubleVar = 2.7;
        System.out.println("Value of double variable \t" + doubleVar);

        // char  variable
        char charVar = 'A';
        System.out.println("Value of char variable \t" + charVar);

        //boolean variable
        boolean boolVar = true;
        System.out.println("Value of boolean variable \t" + boolVar);

    }
}
